import Foundation

extension Int {
    func toString() -> String {
        return String(describing: self)
    }
}

extension Float {
    func toString() -> String {
        return String(describing: self)
    }
}

extension Substring {
    func toString() -> String {
        return String(describing: self)
    }
}
